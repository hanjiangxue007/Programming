#!/usr/bin/env python

# This is called the shebang line.
# (also called a hashbang,hashpling, pound bang, or crunchbang).
#
# In order to run the python script, we need to tell the shell three things:
#
# 1.  That the file is a script
# 2.  Which interpreter we want to execute the script
# 3.  The path of said interpreter
#
# The shebang #! accomplishes (1.). The shebang begins with a #
# because the # character is a comment marker
# in many scripting languages. The contents of the shebang line are
# therefore automatically ignored by the interpreter.
#
# The env command accomplishes (2.) and (3.)

# clear; python bhishan.py 

